#include <unistd.h>

int	main(int argc, char **argv)
{
	int	idx;

	argc += 0;
	idx = -1;
	while (argv[0][++idx] != '\0')
		write(1, &argv[0][idx], 1);
	write(1, "\n", 1);
	return (0);
}
