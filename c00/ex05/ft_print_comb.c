#include <unistd.h>

void	ft_print_comb(void)
{
	char	n[3];
	char	s[2];

	n[0] = '0';
	s[0] = ',';
	s[1] = ' ';
	while (n[0] <= '7')
	{
		n[1] = n[0] + 1;
		while (n[1] <= '8')
		{
			n[2] = n[1] + 1;
			while (n[2] <= '9')
			{
				write(1, &n, 3);
				n[2]++;
				if (n[0] != '7')
				{
					write(1, &s, 2);
				}
			}
			n[1]++;
		}
		n[0]++;
	}
}
