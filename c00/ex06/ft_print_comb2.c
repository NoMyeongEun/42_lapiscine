#include <unistd.h>

void	print(int n1, int n2)
{
	char	res[5];

	res[0] = '0' + n1 / 10;
	res[1] = '0' + n1 % 10;
	res[2] = ' ';
	res[3] = '0' + n2 / 10;
	res[4] = '0' + n2 % 10;
	write(1, &res, 5);
	if (n1 != 98)
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int		n1;
	int		n2;

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			if (n1 < n2)
			{
				print(n1, n2);
			}
			n2++;
		}
		n1++;
	}
}
