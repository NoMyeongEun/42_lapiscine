void	ft_rev_int_tab(int *tab, int size)
{
	int		s;
	int		e;
	int		tmp;

	s = 0;
	e = size - 1;
	while (s < e)
	{
		tmp = tab[s];
		tab[s] = tab[e];
		tab[e] = tmp;
		s++;
		e--;
	}
	return ;
}
