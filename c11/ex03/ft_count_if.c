int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	cnt;
	int	idx;

	cnt = 0;
	idx = -1;
	while (++idx < length)
		if (f(tab[idx]) != 0)
			cnt++;
	return (cnt);
}
