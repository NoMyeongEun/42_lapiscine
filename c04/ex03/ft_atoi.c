int	ft_atoi(char *str)
{
	int	sign;
	int	res;

	sign = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	res = 0;
	while (*str >= '0' && *str <= '9')
	{
		res *= 10;
		res += (sign * (*str - '0'));
		str++;
	}
	return (res);
}
